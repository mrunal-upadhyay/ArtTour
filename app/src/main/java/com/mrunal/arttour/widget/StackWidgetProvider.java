package com.mrunal.arttour.widget;

import android.app.PendingIntent;
import android.appwidget.AppWidgetManager;
import android.appwidget.AppWidgetProvider;
import android.content.Context;
import android.content.Intent;
import android.net.Uri;
import android.util.Log;
import android.widget.RemoteViews;
import com.mrunal.arttour.R;
import com.mrunal.arttour.model.Artwork;
import com.mrunal.arttour.ui.ArtDetailActivity;
import com.mrunal.arttour.utils.ParcelableUtil;

public class StackWidgetProvider extends AppWidgetProvider {

  public static final String INTENT_ACTION = "com.mrunal.arttour.widget.INTENT_ACTION";
  public static final String EXTRA_ART = "com.mrunal.arrttour.widget.EXTRA_ART";

  @Override
  public void onReceive(Context context, Intent intent) {
    if (intent.getAction().equals(INTENT_ACTION)) {

      byte[] bytes = intent.getByteArrayExtra(EXTRA_ART);
      Artwork artwork = ParcelableUtil.unmarshall(bytes, Artwork.CREATOR);

      Log.e("Intent", "->" + artwork.getTitle());
      Intent showArtDetail = new Intent(context, ArtDetailActivity.class);
      showArtDetail.putExtra(ArtDetailActivity.TAG_ARTWORK, artwork);
      showArtDetail.setFlags(Intent.FLAG_ACTIVITY_NEW_TASK);
      context.startActivity(showArtDetail);
    }

    super.onReceive(context, intent);
  }

  @Override
  public void onUpdate(Context context, AppWidgetManager appWidgetManager, int[] appWidgetIds) {

    // update each of the widgets with the remote adapter
    for (int i = 0; i < appWidgetIds.length; ++i) {

      Intent intent = new Intent(context, StackWidgetService.class);
      intent.putExtra(AppWidgetManager.EXTRA_APPWIDGET_ID, appWidgetIds[i]);

      intent.setData(Uri.parse(intent.toUri(Intent.URI_INTENT_SCHEME)));
      RemoteViews rv = new RemoteViews(context.getPackageName(), R.layout.widget_layout);
      rv.setRemoteAdapter(appWidgetIds[i], R.id.stack_view, intent);

      // The empty view is displayed when the collection has no items. It should be a sibling
      // of the collection view.
      rv.setEmptyView(R.id.stack_view, R.id.empty_view);

      // Here we setup the a pending intent template. Individuals items of a collection
      // cannot setup their own pending intents, instead, the collection as a whole can
      // setup a pending intent template, and the individual items can set a fillInIntent
      // to create unique before on an item to item basis.
      Intent artIntent = new Intent(context, StackWidgetProvider.class);
      artIntent.setAction(StackWidgetProvider.INTENT_ACTION);
      artIntent.putExtra(AppWidgetManager.EXTRA_APPWIDGET_ID, appWidgetIds[i]);
      intent.setData(Uri.parse(intent.toUri(Intent.URI_INTENT_SCHEME)));
      PendingIntent toastPendingIntent = PendingIntent
          .getBroadcast(context, 0, artIntent, PendingIntent.FLAG_UPDATE_CURRENT);
      rv.setPendingIntentTemplate(R.id.stack_view, toastPendingIntent);

      appWidgetManager.updateAppWidget(appWidgetIds[i], rv);
    }
    super.onUpdate(context, appWidgetManager, appWidgetIds);
  }
}